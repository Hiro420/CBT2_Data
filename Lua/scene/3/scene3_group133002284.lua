-- 触发条件
function condition_EVENT_GADGET_STATE_CHANGE_383(context, evt)
	if 2379 == evt.param2 and GadgetState.ChestOpened == evt.param1 then
		return true
	end
	
	if 2382 == evt.param2 and GadgetState.ChestOpened == evt.param1 then
		return true
	end
	
	if 2387 == evt.param2 and GadgetState.ChestOpened == evt.param1 then
		return true
	end
	
	return false
end

-- 触发操作
function action_EVENT_GADGET_STATE_CHANGE_383(context, evt)
	-- 调用提示id为 1110004 的提示UI，会显示在屏幕中央偏下位置，id索引自 ReminderData表格
	if 0 ~= ScriptLib.ShowReminder(context, 1110004) then
		return -1
	end
	
	return 0
end

-- 触发条件
function condition_EVENT_GADGET_STATE_CHANGE_384(context, evt)
	if 2379 ~= evt.param2 or GadgetState.ChestOpened ~= evt.param1 then
		return false
	end
	
	return true
end

